package com.practice.smarttech;

public class PracticeEncapsulation {
	private int value;
	
    private String name;

	public int getValue() {// get method is the return method
		return value;
	}

	public void setValue(int value) {// set method is the void method
		this.value = value;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
		
	
	}
	private int salary = 500;

	public int getSalary() {
		return salary;
	}

	public void setSalary(int salary) {
		this.salary = salary;
	}
	private int money;

	public int getMoney() {
		return money;
	}

	public void setMoney(int money) {
		this.money = money;
	}
    }
    

